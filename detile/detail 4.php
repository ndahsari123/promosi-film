                            <p>Alur cerita film kartun Shinbi's House adalah tentang Hari dan Doori, sepasang adik-kakak yang pindah ke Apartemen Shinbi dan bertemu dengan Shinbi, seorang peri goblin berusia 102 tahun.
                                 Shinbi memiliki kekuatan sihir dan menawarkan kesepakatan kepada Hari dan Doori:</p>
                            <p>.Film kartun Shinbi’s House yang ditayangkan di Netflix pada 2017 lalu, kini sudah bisa Anda saksikan di NET TV. Musim pertama film animasi anak-anak ini tayang mulai 15 Agustus 2020, sedangkan musim keduanya mulai ditayangkan pada 1 Maret 2021 lalu. 
                                Shinbi’s House merupakan film kartun asal Korea Selatan yang bergenre komedi, horor, dan petualangan.</p>
                            <h4> SINOPSIS FILM AVATAR THE LAST AIRBENDER</h4>
                            <P>"Shinbi’s House" adalah film animasi yang mengikuti petualangan sekelompok anak-anak yang menemukan sebuah rumah tua yang misterius. Saat mereka menjelajahi rumah tersebut, mereka bertemu dengan Shinbi,
                                makhluk ajaib yang memiliki kekuatan untuk melihat hantu dan entitas supernatural lainnya.</P>
                             <p>Anak-anak tersebut segera menyadari bahwa rumah itu dihuni oleh berbagai roh, dan mereka harus bekerja sama dengan Shinbi untuk mengatasi berbagai tantangan dan teka-teki yang muncul.
                                 Setiap roh memiliki cerita dan alasan mengapa mereka terjebak di rumah itu, dan anak-anak belajar tentang persahabatan, keberanian, dan pentingnya memahami masa lalu.</p>
                            <p>Dengan perpaduan antara humor, petualangan, dan sedikit ketegangan, film ini mengeksplorasi tema persahabatan dan keberanian saat anak-anak berusaha menyelamatkan rumah tersebut
                                 dari ancaman gelap yang ingin mengambil alih. Misi mereka membawa mereka pada petualangan tak terlupakan yang menguji ikatan di antara mereka dan membangun kepercayaan diri mereka.</p>
                            <p>Film ini cocok untuk segala usia dan menghadirkan pesan positif tentang menghadapi ketakutan dan menerima perbedaan.</p>
                        